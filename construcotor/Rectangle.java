package com.rays.construcotor;

public class Rectangle extends Shape {
	private int lengh = 0;
	private int width = 0;
	
	public void setLengh(int lengh) {
		this.lengh = lengh;
	}
	public int getLengh() {
		return lengh;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	public int getWidth() {
		return width;
	}

}
